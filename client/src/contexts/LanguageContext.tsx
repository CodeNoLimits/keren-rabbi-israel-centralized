const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [currentLanguage, setCurrentLanguage] = useState('he');

  useEffect(() => {
    const saved = localStorage.getItem('site-language');
    if (saved && saved in translations) {
      setCurrentLanguage(saved);
    }
  }, []);

  const setLanguage = (language: string) => {
    setCurrentLanguage(language);
    localStorage.setItem('site-language', language);
  };

  const t = (key: string): string => {
    const currentTranslations = translations[currentLanguage as keyof typeof translations] || translations.he;
    return currentTranslations[key as keyof typeof currentTranslations] || key;
  };

  // Task 32: RTL detection for Hebrew and Arabic
  const isRTL = currentLanguage === 'he' || currentLanguage === 'ar';

  return (
    <LanguageContext.Provider value={{ currentLanguage, setLanguage, t, isRTL }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}